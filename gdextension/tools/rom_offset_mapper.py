def build_offset_database(
    registry_path: Path,
    graphics_root: Path,
    output_path: Path,
    ld_script_path: Optional[Path] = None,
    symfile_path: Optional[Path] = None,
    rom_path: Optional[Path] = None
) -> Tuple[int, int]:
    """
    Build the complete offset database.
    
    Returns:
        (found_count, total_count)
    """
    print("Loading asset registry...")
    registry = load_asset_registry(registry_path)
    print(f"  {len(registry)} assets registered")
    
    offsets = {}
    
    # Method 1: Try linker script
    if ld_script_path and ld_script_path.exists():
        print("\nMethod 1: Parsing linker script...")
        ld_offsets = try_parse_ld_script(ld_script_path)
        print(f"  Found {len(ld_offsets)} symbols in linker script")
        
        for entry in registry:
            varname = entry['varname']
            if varname in ld_offsets:
                asset_path = graphics_root / entry['asset_path']
                size = get_asset_binary_size(asset_path) if asset_path.exists() else 0
                offsets[varname] = {
                    'varname': varname,
                    'type': entry['type'],
                    'rom_offset': ld_offsets[varname],
                    'size_bytes': size,
                    'status': 'from_linker_script',
                    'asset_path': entry['asset_path']
                }
    
    # Method 2: Try symbol file
    if symfile_path and symfile_path.exists():
        print("\nMethod 2: Parsing symbol file...")
        sym_offsets = try_extract_from_symfile(symfile_path)
        print(f"  Found {len(sym_offsets)} symbols in symbol file")
        
        for entry in registry:
            varname = entry['varname']
            if varname in sym_offsets and varname not in offsets:
                asset_path = graphics_root / entry['asset_path']
                size = get_asset_binary_size(asset_path) if asset_path.exists() else 0
                offsets[varname] = {
                    'varname': varname,
                    'type': entry['type'],
                    'rom_offset': sym_offsets[varname],
                    'size_bytes': size,
                    'status': 'from_symbol_file',
                    'asset_path': entry['asset_path']
                }
    
    # Method 3: Binary scanning (with or without ROM)
    print("\nMethod 3: Asset binary scanning...")
    rom_data = None
    if rom_path and rom_path.exists():
        print(f"  Loading ROM: {rom_path}")
        with open(rom_path, 'rb') as f:
            rom_data = f.read()
        print(f"  ROM size: {len(rom_data)} bytes")
    
    # Get remaining entries that need offset discovery
    remaining = [e for e in registry if e['varname'] not in offsets]
    if remaining:
        discovered = try_find_offsets_from_assets(remaining, graphics_root, rom_data)
        offsets.update(discovered)
    
    # Count results
    found = sum(1 for v in offsets.values() if v.get('rom_offset') is not None)
    total = len(registry)
    
    # Generate output
    output_list = list(offsets.values())
    
    # Sort by source file for readability
    output_list.sort(key=lambda x: x.get('asset_path', ''))
    
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(output_list, f, indent=2)
    
    return found, total


def generate_embedded_header(offsets_path: Path, header_path: Path):
    """
    Generate a C header file with embedded offset data.
    
    This allows the runtime to load offsets without external files.
    """
    with open(offsets_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Generate C array with offset data
    lines = [
        '#ifndef GBA_ASSET_OFFSETS_H',
        '#define GBA_ASSET_OFFSETS_H',
        '',
        '// Auto-generated by rom_offset_mapper.py',
        '// Do not edit manually',
        '',
        '#include <stdint.h>',
        '',
        'typedef struct {',
        '    const char* varname;',
        '    uint32_t rom_offset;',
        '    uint32_t size_bytes;',
        '    uint8_t type_size;  // 1=u8, 2=u16, 4=u32',
        '} AssetOffsetEntry;',
        '',
        'static const AssetOffsetEntry g_asset_offsets[] = {'
    ]
    
    type_size_map = {'u8': 1, 'u16': 2, 'u32': 4}
    
    for entry in data:
        varname = entry['varname']
        offset = entry.get('rom_offset', 0)
        size = entry.get('size_bytes', 0)
        type_size = type_size_map.get(entry.get('type', 'u8'), 1)
        
        if offset is None:
            offset = 0xFFFFFFFF  # Marker for runtime discovery
        
        lines.append(f'    {{"{varname}", {offset}, {size}, {type_size}}},')
    
    lines.extend([
        '    {NULL, 0, 0, 0}  // Sentinel',
        '};',
        '',
        '#define ASSET_OFFSET_COUNT ' + str(len(data)),
        '#define OFFSET_RUNTIME_DISCOVERY 0xFFFFFFFF',
        '',
        '#endif /* GBA_ASSET_OFFSETS_H */'
    ])
    
    header_path.parent.mkdir(parents=True, exist_ok=True)
    with open(header_path, 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines))


def main():
    parser = argparse.ArgumentParser(
        description='Map pokefirered assets to ROM offsets'
    )
    parser.add_argument(
        '--registry',
        type=Path,
        default=Path('tools/asset_registry.json'),
        help='Path to asset registry JSON (default: tools/asset_registry.json)'
    )
    parser.add_argument(
        '--graphics-root',
        type=Path,
        default=Path('../graphics'),
        help='Root of graphics directory (default: ../graphics)'
    )
    parser.add_argument(
        '--output',
        type=Path,
        default=Path('tools/asset_offsets.json'),
        help='Output JSON file (default: tools/asset_offsets.json)'
    )
    parser.add_argument(
        '--header',
        type=Path,
        default=Path('include/gba_asset_offsets.h'),
        help='Output C header file (default: include/gba_asset_offsets.h)'
    )
    parser.add_argument(
        '--ld-script',
        type=Path,
        help='Path to ld_script.ld for symbol extraction'
    )
    parser.add_argument(
        '--symfile',
        type=Path,
        help='Path to symbol file (.sym) for offset extraction'
    )
    parser.add_argument(
        '--rom',
        type=Path,
        help='Path to GBA ROM for binary fingerprint scanning'
    )
    parser.add_argument(
        '--runtime-discovery',
        action='store_true',
        help='Mark all assets for runtime discovery (no build-time ROM required)'
    )
    
    args = parser.parse_args()
    
    # Ensure paths are absolute
    script_dir = Path(__file__).parent.absolute()
    
    registry_path = (script_dir / args.registry).resolve()
    graphics_root = (script_dir / args.graphics_root).resolve()
    output_path = (script_dir / args.output).resolve()
    header_path = (script_dir / args.header).resolve()
    
    ld_script = (script_dir / args.ld_script).resolve() if args.ld_script else None
    symfile = (script_dir / args.symfile).resolve() if args.symfile else None
    rom_path = None if args.runtime_discovery else (script_dir / args.rom).resolve() if args.rom else None
    
    print("=" * 70)
    print("ROM Offset Mapper for PokeFireRed")
    print("=" * 70)
    print(f"Registry: {registry_path}")
    print(f"Graphics: {graphics_root}")
    print(f"Output:   {output_path}")
    print(f"Header:   {header_path}")
    if ld_script:
        print(f"LD Script: {ld_script}")
    if symfile:
        print(f"Symfile: {symfile}")
    if rom_path:
        print(f"ROM: {rom_path}")
    elif args.runtime_discovery:
        print("Mode: Runtime discovery (all assets)")
    print("=" * 70)
    
    try:
        found, total = build_offset_database(
            registry_path, graphics_root, output_path,
            ld_script, symfile, rom_path
        )
        
        print(f"\nOffset mapping complete:")
        print(f"  Found: {found}/{total} assets ({100*found//total}%)")
        print(f"  Pending runtime discovery: {total - found} assets")
        
        # Generate embedded header
        print(f"\nGenerating embedded header...")
        generate_embedded_header(output_path, header_path)
        print(f"  Written: {header_path}")
        
        return 0
        
    except Exception as e:
        print(f"\nError: {e}")
        import traceback
        traceback.print_exc()
        return 1


if __name__ == '__main__':
    exit(main())
