#!/usr/bin/env python3
"""
rom_offset_mapper.py - Maps assets to ROM offsets

This script attempts to determine GBA ROM offsets for assets using multiple methods:
1. Build-time: Parse ld_script.ld or build artifacts for linked addresses
2. Runtime discovery: Generate offsets.json with placeholder values for runtime scanning

Since pokefirered is a full decompilation, asset offsets can theoretically be determined
from the build system. However, if that's not available, we support runtime discovery
where the game scans the user's ROM at first launch.
"""

import os
import re
import json
import argparse
import struct
from pathlib import Path
from typing import List, Dict, Optional, Tuple


def load_asset_registry(registry_path: Path) -> List[Dict]:
    """Load the asset registry generated by incbin_transform.py"""
    with open(registry_path, 'r', encoding='utf-8') as f:
        return json.load(f)


def try_parse_ld_script(ld_script_path: Path) -> Dict[str, int]:
    """
    Attempt to parse the linker script for asset addresses.
    
    Returns dict mapping symbol names to ROM offsets.
    """
    offsets = {}
    
    if not ld_script_path.exists():
        return offsets
    
    try:
        with open(ld_script_path, 'r', encoding='utf-8') as f:
            content = f.read()
        
        # Look for symbol definitions in linker script
        # Pattern: symbol = 0xXXXXXXXX; or symbol : ORIGIN + length
        symbol_pattern = re.compile(r'(\w+)\s*=\s*(0x[0-9a-fA-F]+)', re.IGNORECASE)
        
        for match in symbol_pattern.finditer(content):
            symbol = match.group(1)
            addr = int(match.group(2), 16)
            # Convert to ROM offset (GBA ROM starts at 0x08000000)
            if addr >= 0x08000000:
                offsets[symbol] = addr - 0x08000000
        
    except Exception as e:
        print(f"Warning: Could not parse ld_script: {e}")
    
    return offsets


def try_extract_from_symfile(sym_path: Path) -> Dict[str, int]:
    """
    Attempt to extract offsets from a symbol file (.sym or similar).
    
    pokefirered builds typically generate .sym files with address information.
    """
    offsets = {}
    
    if not sym_path.exists():
        return offsets
    
    try:
        with open(sym_path, 'r', encoding='utf-8') as f:
            for line in f:
                # Common symbol file format: ADDR TYPE NAME
                # Example: 08012345 T sBagWindowPalF
                parts = line.strip().split()
                if len(parts) >= 3:
                    try:
                        addr = int(parts[0], 16)
                        symbol = parts[2]
                        if addr >= 0x08000000:
                            offsets[symbol] = addr - 0x08000000
                    except ValueError:
                        pass
    except Exception as e:
        print(f"Warning: Could not parse symbol file: {e}")
    
    return offsets


def get_asset_binary_size(asset_path: Path) -> int:
    """Get the size of an asset file in bytes."""
    try:
        return asset_path.stat().st_size
    except:
        return 0


